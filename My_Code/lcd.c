void LCD_ShowString(u16 x,u16 y,const u8 *p,u16 fc,u16 bc,u8 sizey,u8 mode)
{         
	while(*p!='\0')
	{       
		LCD_ShowChar(x,y,*p,fc,bc,sizey,mode);
		x+=sizey/2;
		p++;
	}  
}


/******************************************************************************
      函数说明：显示数字
      入口数据：m底数，n指数
      返回值：  无
******************************************************************************/
u32 mypow(u8 m,u8 n)
{
	u32 result=1;	 
	while(n--)result*=m;
	return result;
}


/******************************************************************************
      函数说明：显示整数变量
      入口数据：x,y显示坐标
                num 要显示整数变量
                len 要显示的位数
                fc 字的颜色
                bc 字的背景色
                sizey 字号
      返回值：  无
******************************************************************************/
void LCD_ShowIntNum(u16 x, u16 y, int num, u8 len, u16 fc, u16 bc, u8 sizey)  
{         
    u8 t, temp;  
    u8 enshow = 0;  // 标志位，用于控制是否开始显示数字  
    u8 sizex = sizey / 2;  // 字符宽度  
    int number = num;  // 临时保存，用于处理负号  

    // 检查是否为负数  
    if (number < 0) {  
        LCD_ShowChar(x, y, '-', fc, bc, sizey, 0);  // 显示负号  
        x += sizex;  // 更新 x 坐标，负号占用一个字符宽度  
        number = -number;  // 取绝对值  
        len--;  // 位数减1，负号占用一个位置  
    }  

    for (t = 0; t < len; t++) {  
        temp = (number / mypow(10, len - t - 1)) % 10;  // 提取当前位上的数字  
        
        if (enshow == 0 && t < (len - 1)) {  
            if (temp == 0) {  
                LCD_ShowChar(x + t * sizex, y, ' ', fc, bc, sizey, 0);  // 显示空格  
                continue;  
            } else {  
                enshow = 1;  // 表示开始显示数字  
            }  
        }  
        LCD_ShowChar(x + t * sizex, y, temp + 48, fc, bc, sizey, 0);  // 显示数字字符  
    }  
} 


/******************************************************************************
      函数说明：显示两位小数变量
      入口数据：x,y显示坐标
                num 要显示小数变量
                len 要显示的位数
                fc 字的颜色
                bc 字的背景色
                sizey 字号
      返回值：  无
******************************************************************************/
void LCD_ShowFloatNum1(u16 x,u16 y,float num,u8 len,u16 fc,u16 bc,u8 sizey)
{         	
	u8 t,temp,sizex;
	u16 num1;
	sizex=sizey/2;
	num1=num*100;
	for(t=0;t<len;t++)
	{
		temp=(num1/mypow(10,len-t-1))%10;
		if(t==(len-2))
		{
			LCD_ShowChar(x+(len-2)*sizex,y,'.',fc,bc,sizey,0);
			t++;
			len+=1;
		}
	 	LCD_ShowChar(x+t*sizex,y,temp+48,fc,bc,sizey,0);
	}
}


/******************************************************************************
      函数说明：显示图片
      入口数据：x,y起点坐标
                length 图片长度
                width  图片宽度
                pic[]  图片数组    
      返回值：  无
******************************************************************************/
void LCD_ShowPicture(u16 x,u16 y,u16 length,u16 width,const u8 pic[])
{
	u16 i,j,k=0;
	LCD_Address_Set(x,y,x+length-1,y+width-1);
	for(i=0;i<length;i++)
	{
		for(j=0;j<width;j++)
		{
			LCD_WR_DATA8(pic[k*2]);
			LCD_WR_DATA8(pic[k*2+1]);
			k++;
		}
	}			
}









